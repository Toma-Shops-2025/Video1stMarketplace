export const useFileContent = () => {
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const getFileContent = useCallback(async (filename: string): Promise<string> => {
    setLoading(true);
    setError(null);
    
    try {
      // Simulate API delay
      await new Promise(resolve => setTimeout(resolve, 500));
      
      if (FILE_CONTENTS[filename]) {
        return FILE_CONTENTS[filename];
      }
      
      // Return placeholder content for files not in our mock data
      return `// Content of ${filename}\n// This file is part of the TomaShops project\n// File path: ${filename}\n\n// This is placeholder content.\n// In a real application, this would contain the actual file content.`;
    } catch (err) {
      const errorMessage = `Failed to load ${filename}`;
      setError(errorMessage);
      throw new Error(errorMessage);
    } finally {
      setLoading(false);
    }
  }, []);

  return { getFileContent, loading, error };
};